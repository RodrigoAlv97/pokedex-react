export default function getColor(value) {
    switch (value) {

        case 'Grass':
            return '#7ac74f'

        case 'Poison':
            return '#9e439e'

        case 'Normal':
            return '#a9a979'

        case 'Fighting':
            return '#c13029'

        case 'Flying':
            return '#a891e9'

        case 'Ground':
            return '#dec06a'

        case 'Rock':
            return '#b6a039'

        case 'Bug':
            return '#a8b920'

        case 'Ghost':
            return '#705699'

        case 'Steel':
            return '#b9b9cf'

        case 'Fire':
            return '#f07e2e'

        case 'Water':
            return '#6890ef'

        case 'Electric':
            return '#f6d030'

        case 'Psychic':
            return '#f05a89'

        case 'Ice':
            return '#98d9d9'

        case 'Dark':
            return '#6f5848'

        case 'Fairy':
            return '#ff62d0'

        case 'Dragon':
            return '#7138fc'

        default:
    }
}